// Mi clase de Java

public class HolaMundo {
    public static void main(String args []) {
        int miVariablEntera = 10;
        System.out.println(miVariablEntera);
        // MODIFICAR LA VARIABLE
        miVariablEntera = 20;
        System.out.println(miVariablEntera);

        String miVariableString = "Hola Raaay";
        System.out.println(miVariableString);

        miVariableString = "Hola Raay 2222";
        System.out.println(miVariableString);

        // var - inferencia de tipos en java

        var miVariablEntera2 = 100;
        System.out.println(miVariablEntera2);

        /// var - en cadena

        var miVariablEntera3 = "Hola Raay en var raaay";
        System.out.println("miVariablEntera3 = " + miVariablEntera3);
    }
}
