import numpy as np

a = np.array([[3, 4, 9]], float)

print (a, "\n-------------------------")

## raiz cuadrada
print (np.sqrt(a))

## exponencial
print (np.exp(a))

## logaritmo
print (np.log(a))

## sin
print (np.sin(a))

## cos
print (np.cos(a))

## media aritmetica
print (np.mean(a))

## vartiacion estandar
print (np.std(a))

## varianza
print (np.var(a))